    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO: Cargar Combobox
        //1. Limpiar combobox
        this.razaCbx.removeAllItems();
        this.planetaCbx.removeAllItems();
        //2. Agregar items al combobox
        String[] razas = new String[]{
            "Sayayin", "Terricola", "Namekuseiyin", "Monstruo"
        };
        String[] planetas = new String[]{
            "Tierra", "Vegita", "Namek", "Otro"
        };

        for (String raza : razas) {
            this.razaCbx.addItem(raza);
        }
        for (String planeta : planetas) {
            this.planetaCbx.addItem(planeta);
        }


    }//GEN-LAST:event_formWindowOpened

    private void limpiarFormulario() {
        this.nombreTxt.setText("");
        this.poderPeleaTxt.setText("");
        this.razaCbx.setSelectedIndex(0);
        this.planetaCbx.setSelectedIndex(0);

    }

    private void ingresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarBtnActionPerformed
        // TODO add your handling code here:
        try {
            Guerrero guerrero = new Guerrero();
            guerrero.setNombre(this.nombreTxt.getText().trim());
            guerrero.setPoderDePelea(Integer
                    .parseInt(this.poderPeleaTxt.getText().trim()));
            guerrero.setPlanetaOrigen(this.planetaCbx
                    .getSelectedItem().toString());
            guerrero.setRaza(this.razaCbx.getSelectedItem().toString());

            GuerreroService guerreroService = new GuerreroService();
            if (guerreroService.ingresarGuerrero(guerrero)) {
                JOptionPane.showMessageDialog(rootPane, "Guerrero Ingresado");
                limpiarFormulario();
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "FAIL");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,
                     "Verifique los datos", "Eror de Validaci√≥n",
                     JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ingresarBtnActionPerformed

